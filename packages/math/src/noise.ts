/**
 * Perlin smootherstep fade curve: `6t⁵ − 15t⁴ + 10t³`. The canonical Perlin fade — same
 * polynomial as {@link easing!smootherstep} but inlined so the noise namespace stays
 * self-contained. Input expected in [0, 1]; output is the smoothed value.
 */
function fade(t: number): number {
  return t * t * t * (t * (t * 6.0 - 15.0) + 10.0);
}

/**
 * Dot product of a 1D gradient with the distance vector. The gradient is +1 or -1 based on
 * the lowest bit of the hash; the distance is the fractional offset from the integer lattice
 * point.
 */
function dotGrad(hash: number, xf: number): number {
  return hash & 0x1 ? xf : -xf;
}

/**
 * 1D Perlin noise. Returns a value in [-1, 1] that varies smoothly with the input `x` —
 * small changes in `x` produce small changes in output (no wild jumps). Three separate calls
 * with different `x` offsets (e.g. `perlin1d(t)`, `perlin1d(t + 100)`, `perlin1d(t + 200)`)
 * produce three independent but individually smooth noise channels.
 *
 * This is the canonical Perlin noise algorithm with the 256-entry permutation table from
 * Bevy's `2d_screen_shake` example. Use for camera shake, procedural terrain, organic
 * motion, and any effect that needs smooth pseudo-random variation.
 */
export function perlin1d(x: number): number {
  const xFloor = Math.floor(x);
  const xi0 = xFloor & 0xff;
  const xi1 = (xFloor + 1) & 0xff;

  const xf0 = x - xFloor;
  const xf1 = xf0 - 1.0;

  const t = fade(xf0);

  const h0 = PERM[xi0] ?? 0;
  const h1 = PERM[xi1] ?? 0;

  const a = dotGrad(h0, xf0);
  const b = dotGrad(h1, xf1);

  // Linear interpolation between the two gradient dots.
  // Using the standard lerp: a + t * (b - a) where t is the faded xf0.
  return a + t * (b - a);
}
