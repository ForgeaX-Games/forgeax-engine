#!/usr/bin/env node
/**
 * scripts/codemod/rename-non-imports.mjs
 *
 * Phase C + D combined: rewrite every `@forgeax/<old>` literal occurrence
 * outside of TS/JS imports (those go through jscodeshift in phase B) and
 * outside of declarative jq-edits (package.json names + tsconfig refs + bin).
 *
 * Covered file types:
 *   - non-import substrings inside .ts / .tsx / .mts / .cts / .mjs / .cjs / .js / .jsx
 *     (grep-gate banned[] arrays, require.resolve, error strings, comments)
 *   - .md  (KB wiki, README.md, CONTRIBUTING.md, AGENTS.md is rewritten by hand later)
 *   - .yml / .yaml (workflows)
 *   - .json (forgeax-metrics.schema.json description, todos.json title/description)
 *
 * Excluded (append-only / generated / unrelated):
 *   - **\/dist/**, **\/node_modules/**, **\/.git/**
 *   - .forgeax-harness/forgeax-loop/<feat>/*.json{,l}   (loop SSOT)
 *   - **\/*-progress.jsonl
 *   - **\/history*                                     (history append-only)
 *   - .knowledge-base/log.md, .knowledge-base/sources/** (ingest facts)
 *   - pnpm-lock.yaml / bun.lock (lockfiles — regenerated by package manager)
 *   - CHANGELOG.md (history-equivalent — no @forgeax/* in it today, but defended)
 *   - .changeset/*.md
 *   - scripts/codemod/rename-map.json (the dictionary itself — old/new pairs)
 *
 * Bare `@forgeax/engine` substring protection (R-3 layer 2):
 *   We process the dictionary in array order so longer / prefixed keys win.
 *   The bare-`@forgeax/engine` rewrite uses a JavaScript regex with a
 *   negative look-ahead `(?![-\w])` — Node's regex engine supports look-around
 *   natively. This is why Node is preferred over `sd` here (Rust regex has
 *   no look-around).
 *
 * Idempotency:
 *   Each file is read, rewritten in memory; written only if content differs.
 *   Re-running on already-renamed content produces zero diff (AC-16).
 *
 * Special-case files (handled with stricter rules to protect non-target content):
 *   - todos.json — only top-level array entries' .title / .description; never
 *     descend into a `history` key.
 *   - .changeset/*.md / CHANGELOG.md / .knowledge-base/log.md — skipped (history).
 *
 * Usage:
 *   node scripts/codemod/rename-non-imports.mjs                 (default: rewrite)
 *   node scripts/codemod/rename-non-imports.mjs --dry-run       (preview, no write)
 *   node scripts/codemod/rename-non-imports.mjs --root <path>
 */
import fs from 'node:fs';
import path from 'node:path';
import url from 'node:url';

const __dirname = path.dirname(url.fileURLToPath(import.meta.url));
const REPO_ROOT = (() => {
  const args = process.argv.slice(2);
  const i = args.indexOf('--root');
  if (i >= 0 && args[i + 1]) return path.resolve(args[i + 1]);
  return path.resolve(__dirname, '..', '..');
})();
const DRY_RUN = process.argv.includes('--dry-run');

const RENAME_MAP_PATH = path.join(__dirname, 'rename-map.json');
const ENTRIES = JSON.parse(fs.readFileSync(RENAME_MAP_PATH, 'utf8'));

// Split entries: anchor-restricted (bare "@forgeax/engine") vs plain.
// Anchor regex prevents the bare-engine rewrite from touching '@forgeax/engine-math'.
const BARE_ENGINE = '@forgeax/engine';
const plainEntries = ENTRIES.filter((e) => e.old !== BARE_ENGINE);
const bareEngineEntry = ENTRIES.find((e) => e.old === BARE_ENGINE) || null;

// Files / dirs excluded from rewriting.
function isExcludedPath(rel) {
  const segs = rel.split(path.sep);
  // Directory-based exclusions
  if (segs.includes('node_modules')) return true;
  if (segs.includes('dist')) return true;
  if (segs.includes('.git')) return true;
  if (segs.includes('.worktrees')) return true;
  if (segs.includes('coverage')) return true;
  // The codemod scripts themselves: they intentionally carry the old/new
  // dictionary keys as code, comments, and string literals. Rewriting them
  // would corrupt the dictionary.
  if (segs[0] === 'scripts' && segs[1] === 'codemod') return true;
  // The @forgeax/engine placeholder (packages/engine/) intentionally keeps
  // the bare-engine name; its files are authored by rename-placeholder.mjs
  // and must not be further rewritten by the textual sweep.
  if (
    segs[0] === 'packages' &&
    segs[1] === 'engine' &&
    (rel === path.join('packages', 'engine', 'package.json') ||
      rel === path.join('packages', 'engine', 'README.md'))
  ) {
    return true;
  }
  // History-equivalent files
  if (rel === 'CHANGELOG.md') return true;
  if (rel === path.join('.knowledge-base', 'log.md')) return true;
  if (
    segs[0] === '.changeset' &&
    rel.endsWith('.md') &&
    !rel.endsWith(path.join('.changeset', 'README.md'))
  )
    return true;
  if (segs[0] === '.knowledge-base' && segs[1] === 'sources') return true;
  // Lockfiles regenerated by package manager
  if (rel === 'pnpm-lock.yaml' || rel === 'bun.lock') return true;
  // closed-loop SSOT: anything under .forgeax-harness/forgeax-loop/<feat>/ is append-only / per-feat.
  // Carve-out: todos.json (the global active-todo ledger) participates in the
  // rename via its schema-aware special handler (title / description rewrite,
  // history left untouched). All other files in this subtree remain excluded.
  // The directory `.forgeax-harness/forgeax-loop` itself must be entered so the
  // walker can reach todos.json; per-feat subdirectories (segs[2] !== undefined
  // and not 'todos.json') stay excluded.
  if (segs[0] === '.forgeax-harness' && segs[1] === 'forgeax-loop') {
    // The forgeax-loop directory itself (segs.length === 2): enter it.
    if (segs.length === 2) return false;
    // todos.json directly under forgeax-loop: include.
    if (rel === path.join('.forgeax-harness', 'forgeax-loop', 'todos.json')) {
      return false;
    }
    // Everything else under forgeax-loop/ (per-feat dirs, jsonl logs, etc.): skip.
    return true;
  }
  // any *-progress.jsonl or history*
  if (rel.endsWith('-progress.jsonl')) return true;
  const base = path.basename(rel);
  if (base.startsWith('history')) return true;
  return false;
}

// Files where the bare `@forgeax/engine` literal is intentional (placeholder
// discussion in repo-root docs). The non-bare entries in rename-map still apply
// to these files; only the bare-engine look-ahead rewrite is suppressed.
function isBareEngineExempt(rel) {
  if (rel === 'AGENTS.md') return true;
  if (rel === 'README.md') return true;
  // Design docs that discuss the @forgeax/engine placeholder by name
  // (handover spec proposing to upgrade the placeholder into a preset/BOM,
  // and its referenced spike). Rewriting the bare token to @forgeax/engine-runtime
  // destroys the design (e.g. "rename package name to @forgeax/engine to take
  // over the placeholder" becomes a tautology).
  if (rel === path.join('docs', 'studio-engine-preset.md')) return true;
  if (rel === path.join('docs', 'studio-runtime-spike.md')) return true;
  // The bun-workspace consumer how-to has a section explaining the
  // packages/engine/ placeholder + its `@forgeax/engine` family-root name
  // and why it co-exists with the @forgeax/engine-* children. Rewriting
  // the bare token to @forgeax/engine-runtime collapses the explanation
  // (the whole point is that `@forgeax/engine` and `@forgeax/engine-runtime`
  // are distinct names that bun install treats as separate packages).
  if (rel === path.join('docs', 'how-to', '2026-05-23-consuming-from-bun-workspace.md')) {
    return true;
  }
  return false;
}

// Extension dispatch: which files we even consider.
const TEXT_EXTENSIONS = new Set([
  '.ts',
  '.tsx',
  '.mts',
  '.cts',
  '.js',
  '.jsx',
  '.mjs',
  '.cjs',
  '.md',
  '.yml',
  '.yaml',
  '.json',
  '.toml',
  '.sh',
  '.rs',
]);

function walk(dir, out, rel = '') {
  let entries;
  try {
    entries = fs.readdirSync(dir, { withFileTypes: true });
  } catch {
    return;
  }
  for (const ent of entries) {
    const childRel = rel ? path.join(rel, ent.name) : ent.name;
    if (isExcludedPath(childRel)) continue;
    const full = path.join(dir, ent.name);
    if (ent.isDirectory()) {
      walk(full, out, childRel);
    } else if (ent.isFile()) {
      const ext = path.extname(ent.name);
      if (TEXT_EXTENSIONS.has(ext)) out.push(childRel);
    }
  }
}

/**
 * Rewrite plain text content. Order: all non-engine entries first (in dictionary
 * order), then bare-engine with look-ahead anchor. Handles TS/JS files too —
 * but limits substitution to *non-import-like* strings? No: the jscodeshift
 * transform already runs first and any TS/JS import literal has already been
 * rewritten. After jscodeshift, the remaining @forgeax/<old> hits in TS/JS
 * source are non-import strings (banned[] arrays, error messages, comments).
 * Running this textual pass on TS/JS is therefore safe and idempotent.
 */
function rewriteText(content, opts = {}) {
  let out = content;
  for (const e of plainEntries) {
    if (out.indexOf(e.old) === -1) continue;
    // Replace whole-string occurrences; rely on dictionary order to avoid
    // partial collisions among @forgeax/<old> entries.
    out = out.split(e.old).join(e.new);
  }
  if (!opts.skipBareEngine && bareEngineEntry && out.indexOf(bareEngineEntry.old) !== -1) {
    // Look-ahead protected: do not rewrite when followed by '-' or word char.
    const re = /@forgeax\/engine(?![-\w])/g;
    out = out.replace(re, bareEngineEntry.new);
  }
  return out;
}

/**
 * Special handler for todos.json — only rewrite .todos[].title and
 * .todos[].description. Never descend into .todos[].history (append-only).
 */
function rewriteTodosJson(raw) {
  const data = JSON.parse(raw);
  if (!Array.isArray(data.todos)) return raw;
  let mutated = false;
  for (const todo of data.todos) {
    for (const field of ['title', 'description']) {
      if (typeof todo[field] === 'string') {
        const next = rewriteText(todo[field]);
        if (next !== todo[field]) {
          todo[field] = next;
          mutated = true;
        }
      }
    }
    // Deliberately not touching todo.history.
  }
  if (!mutated) return raw;
  // Preserve trailing newline if present.
  const trailingNewline = raw.endsWith('\n') ? '\n' : '';
  return JSON.stringify(data, null, 2) + trailingNewline;
}

function processFile(absPath, rel) {
  const raw = fs.readFileSync(absPath, 'utf8');
  let next;
  if (rel === 'todos.json' || rel === path.join('.forgeax-harness', 'forgeax-loop', 'todos.json')) {
    next = rewriteTodosJson(raw);
  } else {
    next = rewriteText(raw, { skipBareEngine: isBareEngineExempt(rel) });
  }
  if (next === raw) return { changed: false };
  if (!DRY_RUN) fs.writeFileSync(absPath, next, 'utf8');
  return { changed: true };
}

function main() {
  const files = [];
  walk(REPO_ROOT, files);
  let changed = 0;
  let scanned = 0;
  for (const rel of files) {
    const abs = path.join(REPO_ROOT, rel);
    scanned += 1;
    const r = processFile(abs, rel);
    if (r.changed) {
      changed += 1;
      if (DRY_RUN) console.warn(`[would-edit] ${rel}`);
    }
  }
  console.warn(`[rename-non-imports] scanned=${scanned} changed=${changed} dry-run=${DRY_RUN}`);
}

main();
