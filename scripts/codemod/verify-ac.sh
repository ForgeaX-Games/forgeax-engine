# AC-17: dual-lockfile parity — `bun install --frozen-lockfile --ignore-scripts` exits 0.
# Charter convention (AGENTS.md §Conventions): "Dual lockfile: pnpm-lock.yaml + bun.lock
# both committed; pnpm run sync if drift". If bun.lock drifts, the CI bun matrix job
# (ci.yml `bun: Install (frozen)` step) fails with `lockfile had changes, but lockfile is frozen`.
# Codemod orchestrator Phase H now regenerates bun.lock; this AC pins the parity.
# Skipped automatically if `bun` is not on PATH (env-dependent).
verify_ac_17() {
  if ! command -v bun >/dev/null 2>&1; then
    echo "[AC-17] SKIP (bun not on PATH)"
    return 0
  fi
  if bun install --frozen-lockfile --ignore-scripts >/tmp/ac-17.log 2>&1; then
    pass 17
  else
    fail 17 "bun install --frozen-lockfile --ignore-scripts failed; see /tmp/ac-17.log"
  fi
}

# AC-16: codemod idempotency — re-run produces zero diff.
# Pre-check ignores changes under .forgeax-harness/forgeax-loop/<feat>/ since
# those are append-only closed-loop SSOT artifacts (jsonl logs, decisions
# markdown) maintained orthogonally to the codemod surface.
verify_ac_16() {
  local dirty
  dirty=$(git status --porcelain | rg -v '^\?\? \.forgeax-harness/forgeax-loop/' | rg -v '^.. \.forgeax-harness/forgeax-loop/' || true)
  if [ -n "$dirty" ]; then
    fail 16 "working tree dirty (codemod surface); cannot verify idempotency: $dirty"
    return
  fi
  bash scripts/codemod/rename-engine-family.sh >/tmp/ac-16.log 2>&1
  # Diff ignores loop-state artifacts on the same grounds.
  if git diff --quiet -- ':!.forgeax-harness/forgeax-loop'; then
    pass 16
  else
    fail 16 "re-running codemod produced a non-empty diff (see git diff -- ':!.forgeax-harness/forgeax-loop'); /tmp/ac-16.log for codemod stdout"
  fi
}

main() {
  echo "[verify-ac] verifying 14 automated ACs in $REPO_ROOT"
  echo ""
  verify_ac_01
  verify_ac_02
  verify_ac_03
  verify_ac_06
  verify_ac_08
  verify_ac_09
  verify_ac_10
  verify_ac_11
  verify_ac_12
  verify_ac_13
  verify_ac_17
  # Heavy / build-dependent ACs at the end so failure shows quickly above.
  verify_ac_04
  verify_ac_14
  verify_ac_05
  verify_ac_16

  echo ""
  echo "[verify-ac] PASS=$PASS FAIL=$FAIL"
  if [ "$FAIL" -gt 0 ]; then
    echo "[verify-ac] failing ACs: ${FAIL_NAMES[*]}" >&2
    exit 1
  fi
}

main "$@"
