#!/usr/bin/env bash
# scripts/codemod/verify-ac.sh — 14 automated AC oracle for
# feat-20260511-engine-package-family-rename.
#
# Covers AC-01, AC-02, AC-03, AC-04, AC-05, AC-06, AC-08, AC-09, AC-10,
# AC-11, AC-12, AC-13, AC-14, AC-16, AC-17.
# Skipped (human-read): AC-07 (AGENTS.md §Packages walkthrough),
# AC-15 (requirements-decisions.md D-NN narrative).
#
# Each AC is encapsulated in a `verify_ac_NN` function that returns 0 on pass
# and prints `[AC-NN] FAIL: <reason>` to stderr on failure. Main loop runs all
# checks, accumulates pass / fail counts, and exits non-zero if any failed.
#
# Charter command-4: explicit failure. `set -euo pipefail` would exit on the
# first failure; we instead want to run all checks and aggregate, so the
# script uses `|| true` per check and tracks a fail counter.
set -uo pipefail

REPO_ROOT="$(git rev-parse --show-toplevel)"
cd "$REPO_ROOT"

# rg is required only by this script (rename-engine-family.sh and its
# subscripts use node + jq + jscodeshift, no rg). Fail fast with a hint
# rather than letting the first `rg` invocation crash with command-not-found.
if ! command -v rg >/dev/null 2>&1; then
  echo "[fatal] 'rg' (ripgrep) not installed. Install via:" >&2
  echo "  macOS: 'brew install ripgrep'  ·  Ubuntu: 'sudo apt-get install -y ripgrep'" >&2
  exit 1
fi

PASS=0
FAIL=0
FAIL_NAMES=()

pass() { echo "[AC-$1] PASS" ; PASS=$((PASS+1)) ; }
fail() { echo "[AC-$1] FAIL: $2" >&2 ; FAIL=$((FAIL+1)) ; FAIL_NAMES+=("AC-$1") ; }

# AC-01: 14 workspace packages have correct new names (+ console + template + engine placeholder).
verify_ac_01() {
  local expected_names=(
    "@forgeax/engine"
    "@forgeax/engine-runtime"
    "@forgeax/engine-math"
    "@forgeax/engine-ecs"
    "@forgeax/engine-types"
    "@forgeax/engine-rhi"
    "@forgeax/engine-rhi-webgpu"
    "@forgeax/engine-rhi-wgpu"
    "@forgeax/engine-wgpu-wasm"
    "@forgeax/engine-naga"
    "@forgeax/engine-shader"
    "@forgeax/engine-shader-compiler"
    "@forgeax/engine-vite-plugin-shader"
    "@forgeax/engine-console"
    "@forgeax/engine-template-threejs-mvp"
  )
  local actual
  actual=$(jq -r '.name' packages/*/package.json templates/*/package.json 2>/dev/null | sort -u)
  local missing=()
  for n in "${expected_names[@]}"; do
    if ! echo "$actual" | grep -qx "$n"; then
      missing+=("$n")
    fi
  done
  if [ ${#missing[@]} -eq 0 ]; then
    pass 01
  else
    fail 01 "missing names: ${missing[*]}"
  fi
}

# AC-02: @forgeax/core is gone from source / docs / configs. Carve out:
# - bun.lock / pnpm-lock.yaml: regenerated by package manager, no source-of-truth
# - scripts/codemod/**: dictionary of old→new entries (intentional)
# - scripts/__tests__/fixtures/grep-gates/**: canonical fixtures pre/post rewrite
verify_ac_02() {
  local hits
  hits=$(rg -n --no-heading "@forgeax/core" \
    --glob '!**/*-progress.jsonl' \
    --glob '!**/history*' \
    --glob '!.forgeax-harness/forgeax-loop/**' \
    --glob '!.changeset/**' \
    --glob '!**/node_modules/**' \
    --glob '!**/dist/**' \
    --glob '!**/.git/**' \
    --glob '!.knowledge-base/log.md' \
    --glob '!.knowledge-base/sources/**' \
    --glob '!CHANGELOG.md' \
    --glob '!bun.lock' \
    --glob '!pnpm-lock.yaml' \
    --glob '!scripts/codemod/**' \
    --glob '!scripts/__tests__/fixtures/grep-gates/**' \
    || true)
  if [ -z "$hits" ]; then
    pass 02
  else
    fail 02 "remaining @forgeax/core hits: $(echo "$hits" | head -5 | tr '\n' ';')"
  fi
}

# AC-03: All bare-name imports rewritten to @forgeax/engine-*.
verify_ac_03() {
  local pattern="from ['\"]@forgeax/(math|ecs|core|types|rhi|rhi-webgpu|rhi-wgpu|wgpu-wasm|naga|shader|shader-compiler|vite-plugin-shader|engine|console)['\"]"
  local hits
  hits=$(rg -n --no-heading "$pattern" \
    --glob '!**/node_modules/**' \
    --glob '!**/dist/**' \
    --glob '!**/.git/**' \
    --glob '!**/*-progress.jsonl' \
    --glob '!.forgeax-harness/forgeax-loop/**' \
    --glob '!.knowledge-base/sources/**' \
    --glob '!scripts/codemod/**' \
    --glob '!scripts/__tests__/fixtures/grep-gates/**' \
    || true)
  if [ -z "$hits" ]; then
    pass 03
  else
    fail 03 "remaining legacy imports:\n$hits"
  fi
}

# AC-04: pnpm install + pnpm -r build exit 0 (lockfile up to date already gated by sync:check).
verify_ac_04() {
  if pnpm install --frozen-lockfile >/tmp/ac-04.log 2>&1 && \
     pnpm -r build >>/tmp/ac-04.log 2>&1; then
    pass 04
  else
    fail 04 "pnpm install --frozen-lockfile || pnpm -r build failed; see /tmp/ac-04.log"
  fi
}

# AC-05: smoke gate three commands. These are expensive (Dawn + browser + GPU);
# allow the caller to set SKIP_AC05=1 to skip in dev iteration.
verify_ac_05() {
  if [ "${SKIP_AC05:-0}" = "1" ]; then
    echo "[AC-05] SKIP (SKIP_AC05=1)"
    return 0
  fi
  if pnpm --filter @forgeax/hello-triangle smoke >/tmp/ac-05-smoke.log 2>&1 && \
     pnpm test:browser >/tmp/ac-05-browser.log 2>&1 && \
     pnpm test:dawn >/tmp/ac-05-dawn.log 2>&1; then
    pass 05
  else
    fail 05 "smoke gate failed; logs at /tmp/ac-05-{smoke,browser,dawn}.log"
  fi
}

# AC-06: metrics:check exit 0.
verify_ac_06() {
  if pnpm metrics:check >/tmp/ac-06.log 2>&1; then
