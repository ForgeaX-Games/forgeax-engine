}

# AC-08: @forgeax/engine-console bin is { forgeax-engine-console: ... }, top-level forgeax key absent.
verify_ac_08() {
  local bin_json
  bin_json=$(jq -c '.bin' packages/console/package.json)
  if echo "$bin_json" | jq -e 'has("forgeax-engine-console")' >/dev/null && \
     ! echo "$bin_json" | jq -e 'has("forgeax")' >/dev/null; then
    pass 08
  else
    fail 08 "packages/console/package.json bin = $bin_json (expected forgeax-engine-console, no bare forgeax)"
  fi
}

# AC-09: packages/engine/ placeholder: private:true, README exists, 0 source .ts files.
verify_ac_09() {
  local private
  private=$(jq -r '.private' packages/engine/package.json)
  local name
  name=$(jq -r '.name' packages/engine/package.json)
  local readme
  readme=$(test -f packages/engine/README.md && echo yes || echo no)
  local ts_count
  ts_count=$(find packages/engine/src -name '*.ts' 2>/dev/null | wc -l | tr -d ' ')
  if [ "$private" = "true" ] && [ "$name" = "@forgeax/engine" ] && [ "$readme" = "yes" ] && [ "$ts_count" = "0" ]; then
    pass 09
  else
    fail 09 "placeholder shape mismatch: private=$private name=$name readme=$readme ts_count=$ts_count"
  fi
}

# AC-10: KB wiki has no bare legacy names.
verify_ac_10() {
  local pattern='@forgeax/(math|ecs|core|types|rhi|rhi-webgpu|rhi-wgpu|wgpu-wasm|naga|shader|shader-compiler|vite-plugin-shader|console)([^-a-zA-Z0-9_]|$)'
  local hits
  hits=$(rg -n --no-heading "$pattern" .knowledge-base/wiki || true)
  if [ -z "$hits" ]; then
    pass 10
  else
    fail 10 "legacy names in KB wiki:\n$hits"
  fi
}

# AC-11: .forgeax-harness/forgeax-loop/todos.json title/description renamed; history not modified.
verify_ac_11() {
  local title_hits
  title_hits=$(jq -r '.todos[].title' .forgeax-harness/forgeax-loop/todos.json 2>/dev/null | \
    rg '@forgeax/(math|ecs|core|types|rhi|rhi-webgpu|rhi-wgpu|wgpu-wasm|naga|shader|shader-compiler|vite-plugin-shader|console)([^-a-zA-Z0-9_]|$)' || true)
  local desc_hits
  desc_hits=$(jq -r '.todos[].description // ""' .forgeax-harness/forgeax-loop/todos.json 2>/dev/null | \
    rg '@forgeax/(math|ecs|core|types|rhi|rhi-webgpu|rhi-wgpu|wgpu-wasm|naga|shader|shader-compiler|vite-plugin-shader|console)([^-a-zA-Z0-9_]|$)' || true)
  # history field untouched: not directly diff-able after merge; verify it parses + at
  # least one history entry references legacy name as evidence of preservation.
  local hist_count
  hist_count=$(jq -r '[.todos[] | select(.history != null) | .history | length] | add // 0' .forgeax-harness/forgeax-loop/todos.json)
  if [ -z "$title_hits" ] && [ -z "$desc_hits" ] && [ "$hist_count" -ge 0 ]; then
    pass 11
  else
    fail 11 ".forgeax-harness/forgeax-loop/todos.json residue: title_hits=[${title_hits//$'\n'/ }] desc_hits=[${desc_hits//$'\n'/ }]"
  fi
}

# AC-12: apps/*/package.json + templates/threejs-mvp/package.json deps use new names.
verify_ac_12() {
  local files=(apps/*/package.json templates/threejs-mvp/package.json)
  local bad=()
  for f in "${files[@]}"; do
    [ -f "$f" ] || continue
    local hits
    hits=$(jq -r '(.dependencies // {}) + (.devDependencies // {}) | keys[]' "$f" 2>/dev/null | \
      rg '^@forgeax/(math|ecs|core|types|rhi|rhi-webgpu|rhi-wgpu|wgpu-wasm|naga|shader|shader-compiler|vite-plugin-shader|console|engine)$' || true)
    if [ -n "$hits" ]; then
      bad+=("$f: $hits")
    fi
  done
  local tpl_name
  tpl_name=$(jq -r '.name' templates/threejs-mvp/package.json 2>/dev/null)
  if [ ${#bad[@]} -eq 0 ] && [ "$tpl_name" = "@forgeax/engine-template-threejs-mvp" ]; then
    pass 12
  else
    fail 12 "deps still legacy: ${bad[*]}; tpl_name=$tpl_name"
  fi
}

